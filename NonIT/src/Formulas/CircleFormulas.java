package Formulas;

public class CircleFormulas {
	public static void main(String[] args) {
		
		
		int r = 10; 
		float pi=3.14f;
		
		float area = pi*r*r; 
		System.out.println(area);
		
		float circumference = 2*pi*r;
		System.out.println(circumference);
		
		int diameter = 2*r;
		System.out.println(diameter);
	}

}
